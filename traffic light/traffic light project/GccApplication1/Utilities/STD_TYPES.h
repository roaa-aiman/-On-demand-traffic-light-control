#ifndef STD_TYPES_H_
#define STD_TYPES_H_


/* Signed Data Types */
typedef unsigned char           u8;
typedef unsigned short int      u16;
typedef unsigned long int       u32;

/* Unsigned Data Types */
typedef signed char             s8;
typedef signed short int        s16;
typedef signed long int         s32;

typedef float   f32;
typedef double  f64;
#define NULL 0

#define OK  0
#define NOK  1
#define NULL_POINTER 2



#endif /* STD_TYPES_H_ */